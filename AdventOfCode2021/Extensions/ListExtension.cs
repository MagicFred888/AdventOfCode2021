namespace AdventOfCode2021.Extensions;

public static class ListExtension
{
    public static void Shuffle<T>(this List<T> list)
    {
        Random rng = new();
        int n = list.Count;
        while (n > 1)
        {
            n--;
            int k = rng.Next(n + 1);
            (list[n], list[k]) = (list[k], list[n]);
        }
    }
}